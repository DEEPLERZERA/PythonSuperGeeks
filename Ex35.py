fat = 1 #Atribuindo valor a variável
n = int(input("Digite um valor inteiro: "))  #Entrando com dados

for x in range(n):  #Criando laço de repetição
        x += 1  #Fazendo incremento
        fat *= x    #Calculando fatorial
       
    

print("O fatorial de: ", x, " é ", fat)   #Imprimindo na tela  