semana = int(input("Digite um número para descobrir o dia da semana: a partir de 1 até 7: "))

if semana == 1:
    print("Domingo")
elif semana == 2:
    print("Segunda")
elif semana == 3:
    print("Terça")
elif semana == 4:
    print("Quarta")
elif semana == 5:
    print("Quinta")
elif semana == 6:
    print("Sexta")
elif semana == 7:
    print("Sábado")
else:
    print("Valor invalido")    
